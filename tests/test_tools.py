class TestToQso:
    def test_basic_conversion(self):
        """EQSL-L2-011: Raw dict converted to QsoRecord."""
        rec = {"CALL": "KI7MT", "BAND": "20M", "MODE": "FT8"}
        qso = to_qso(rec)
        assert qso["call"] == "KI7MT"
        assert qso["band"] == "20M"
        assert qso["mode"] == "FT8"

    def test_call_uppercased(self):
        """EQSL-L2-012: Callsign normalized to uppercase."""
        qso = to_qso({"CALL": "ki7mt"})
        assert qso["call"] == "KI7MT"

    def test_missing_fields_default(self):
        """EQSL-L2-013: Missing optional fields return None."""
        qso = to_qso({"CALL": "KI7MT"})
        assert qso["band"] is None
        assert qso["gridsquare"] is None

    def test_freq_float_conversion(self):
        """EQSL-L2-014: FREQ field converted to float."""
        qso = to_qso({"CALL": "KI7MT", "FREQ": "14.074"})
        assert qso["freq"] == pytest.approx(14.074)

    def test_invalid_freq(self):
        """EQSL-L2-015: Invalid FREQ returns None."""
        qso = to_qso({"CALL": "KI7MT", "FREQ": "abc"})
        assert qso["freq"] is None



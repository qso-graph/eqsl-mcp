# ---------------------------------------------------------------------------
# EQSL-L2-001..010: ADIF parser (parse_adif)
# ---------------------------------------------------------------------------


class TestParseAdif:
    def test_single_record(self):
        """EQSL-L2-001: Parse one complete ADIF record."""
        adif = "<CALL:5>KI7MT<BAND:3>20M<EOR>"
        recs = parse_adif(adif)
        assert len(recs) == 1
        assert recs[0]["CALL"] == "KI7MT"
        assert recs[0]["BAND"] == "20M"

    def test_multiple_records(self):
        """EQSL-L2-002: Parse two records separated by EOR."""
        adif = "<CALL:5>KI7MT<EOR><CALL:4>W1AW<EOR>"
        recs = parse_adif(adif)
        assert len(recs) == 2
        assert recs[0]["CALL"] == "KI7MT"
        assert recs[1]["CALL"] == "W1AW"

    def test_case_insensitive_tags(self):
        """EQSL-L2-003: Tags stored uppercase regardless of input case."""
        adif = "<call:5>KI7MT<eor>"
        recs = parse_adif(adif)
        assert recs[0]["CALL"] == "KI7MT"

    def test_empty_string(self):
        """EQSL-L2-004: Empty string returns empty list."""
        assert parse_adif("") == []

    def test_no_eor(self):
        """EQSL-L2-005: Record without EOR still captured."""
        adif = "<CALL:5>KI7MT<BAND:3>20M"
        recs = parse_adif(adif)
        assert len(recs) == 1
        assert recs[0]["CALL"] == "KI7MT"

    def test_value_length_respected(self):
        """EQSL-L2-006: Length field controls value extraction."""
        adif = "<CALL:3>KI7MT<EOR>"
        recs = parse_adif(adif)
        # Parser extracts based on length field
        assert len(recs) == 1
        assert recs[0]["CALL"].startswith("KI7")

    def test_data_type_indicator(self):
        """EQSL-L2-007: Optional type indicator handled."""
        adif = "<QSO_DATE:8:D>20260301<EOR>"
        recs = parse_adif(adif)
        assert recs[0]["QSO_DATE"] == "20260301"

    def test_whitespace_between_tags(self):
        """EQSL-L2-008: Whitespace between tags skipped."""
        adif = "  <CALL:5>KI7MT<BAND:3>20M<EOR>  "
        recs = parse_adif(adif)
        assert len(recs) == 1
        assert recs[0]["CALL"].strip() == "KI7MT"

    def test_eor_only(self):
        """EQSL-L2-009: EOR with no fields produces no records."""
        assert parse_adif("<EOR>") == []

    def test_header_skipped(self):
        """EQSL-L2-010: Text before first tag is ignored."""
        adif = "Generated by eQSL\n<CALL:5>KI7MT<EOR>"
        recs = parse_adif(adif)
        assert len(recs) == 1



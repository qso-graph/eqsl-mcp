class TestDateHelpers:
    def test_to_yyyymmddhhmm_iso(self):
        """EQSL-L2-016: ISO date string converted."""
        result = to_yyyymmddhhmm("2026-03-01")
        assert result == "202603010000"

    def test_to_yyyymmddhhmm_date_obj(self):
        """EQSL-L2-017: date object converted."""
        result = to_yyyymmddhhmm(date(2026, 3, 1))
        assert result == "202603010000"

    def test_to_yyyymmddhhmm_none(self):
        """EQSL-L2-018: None returns 30 days ago."""
        result = to_yyyymmddhhmm(None)
        assert len(result) == 12
        assert result.endswith("0000")

    def test_to_yyyymmddhhmm_invalid(self):
        """EQSL-L2-019: Invalid string falls back to now."""
        result = to_yyyymmddhhmm("not-a-date")
        assert len(result) == 12

    def test_to_mmddyyyy_valid(self):
        """EQSL-L2-020: ISO date to MM/DD/YYYY."""
        assert to_mmddyyyy("2026-03-01") == "03/01/2026"

    def test_to_mmddyyyy_invalid(self):
        """EQSL-L2-021: Invalid date returns input unchanged."""
        assert to_mmddyyyy("not-a-date") == "not-a-date"


